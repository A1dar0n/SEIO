                <h2 class="section-title" style="color:black;">
                    SEIO
                    <span>- Service d’Electronique et d’Informatique de l’Ouest</span>
                </h2>
                <h3 class="section-sub-title">
                    /Présentation_
                </h3>
                
                <p>
                    Depuis 1983, le SEIO - Junior-Entreprise de l’ESEO à Angers - s’inscrit dans le mouvement international des Junior-Entreprises. Notre structure propose des projets dans les domaines de notre grande école d'ingénieur généraliste en électronique et informatique.
                    La marque Junior-Entreprise représente aujourd’hui plus de 200 structures en France. Ces structures sont principalement implantées au sein de Grandes Écoles et Universités du territoire. Avec un chiffre d’affaire cumulé de 9 millions d'euros par an, les Junior-Entreprises permettent aux étudiants d'entrer en contact avec le monde professionnel tout en mettant en pratique leurs enseignements théoriques.
                </p>
                    
                <p>
                    Nous disposons d'étudiants et d'intervenants aux profils et aux compétences variées, forts d’un enseignement pluridisciplinaire lié aux NTIC, enseigné par des spécialistes dans leur domaine.
                    Nous proposons nos services et notre savoir-faire aux professionnels dans tous nos domaines, de la réalisation de sites internet à la conception et au développement d’applications mobiles sur iOS et Android, en passant aussi par la conception de systèmes électroniques et de systèmes embarqués.
                    Nos administrateurs sont là pour vous accompagner et vous conseiller dans le développement de vos projets.
                </p>
