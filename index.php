                <h2 class="section-title" style="color:black;">
                    SEIO
                </h2>
                <h3 class="section-sub-title">
                    /Prix de la meilleure étude d’ingénierie -  Année 2019_ 
                </h3>
                
                <p>
                    Avec plus d’une centaine d’équipes candidates, la Junior-Entreprise SEIO a remporté le Prix de la Meilleure Étude en Ingénierie lors du Congrès National d’Été 2019 de la Confédération Nationale des Junior-Entreprises (CNJE). En compétition avec des projets de Centrale Supelec et de l'Insa Lyon, le SEIO s’est démarqué par la qualité de son étude. Les candidats devaient présenter un dossier complet abordant le contexte d'une étude du mandat passé. Les étudiants de l’ESEO avaient choisi de mettre en avant un projet d'électronique permettant la détection des écarts d’ouverture des stalles de départ pour les courses de chevaux.
                </p>
